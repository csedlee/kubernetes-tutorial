#!/bin/bash

if [ "$ISTIO_DIR" = "" ]; then
    echo 'ISTIO_DIR environment is not set. Example: /home/kubeuser/istio-1.0.5'
    exit
fi


echo 'Deploy httpbin-v1'

cat <<EOF | $ISTIO_DIR/bin/istioctl kube-inject -f - | kubectl create -n $DEFAULT_ISTIO_NAMESPACE -f -
apiVersion: extensions/v1beta1
kind: Deployment
metadata:
  name: httpbin-v1
spec:
  replicas: 1
  template:
    metadata:
      labels:
        app: httpbin
        version: v1
    spec:
      containers:
      - image: docker.io/kennethreitz/httpbin
        imagePullPolicy: IfNotPresent
        name: httpbin
        command: ["gunicorn", "--access-logfile", "-", "-b", "0.0.0.0:8080", "httpbin:app"]
        ports:
        - containerPort: 8080
EOF


echo 'Deploy httpbin-v2'

cat <<EOF | $ISTIO_DIR/bin/istioctl kube-inject -f - | kubectl create -n $DEFAULT_ISTIO_NAMESPACE -f -
apiVersion: extensions/v1beta1
kind: Deployment
metadata:
  name: httpbin-v2
spec:
  replicas: 1
  template:
    metadata:
      labels:
        app: httpbin
        version: v2
    spec:
      containers:
      - image: docker.io/kennethreitz/httpbin
        imagePullPolicy: IfNotPresent
        name: httpbin
        command: ["gunicorn", "--access-logfile", "-", "-b", "0.0.0.0:8080", "httpbin:app"]
        ports:
        - containerPort: 8080
EOF


echo 'create httpbin service'
cat <<EOF | kubectl create -n $DEFAULT_ISTIO_NAMESPACE -f -
apiVersion: v1
kind: Service
metadata:
  name: httpbin
  labels:
    app: httpbin
spec:
  ports:
  - name: http
    port: 8080
  selector:
    app: httpbin
EOF

echo 'Create the sleep service'
cat <<EOF | $ISTIO_DIR/bin/istioctl kube-inject -f - | kubectl create -n $DEFAULT_ISTIO_NAMESPACE -f -
apiVersion: extensions/v1beta1
kind: Deployment
metadata:
  name: sleep
spec:
  replicas: 1
  template:
    metadata:
      labels:
        app: sleep
    spec:
      containers:
      - name: sleep
        image: tutum/curl
        command: ["/bin/sleep","infinity"]
        imagePullPolicy: IfNotPresent
EOF

while [ $(kubectl get pods -n $DEFAULT_ISTIO_NAMESPACE| grep -E 'sleep|httpbin' | grep 'Running' | wc -l) -lt 3 ]; do
  kubectl get pods -n $DEFAULT_ISTIO_NAMESPACE
  echo 'Sleeping until Running...'
  sleep 4
done

#
# Routing Policy only to V1
#

cat <<EOF | kubectl -n $DEFAULT_ISTIO_NAMESPACE apply -f -
apiVersion: networking.istio.io/v1alpha3
kind: VirtualService
metadata:
  name: httpbin
spec:
  hosts:
    - httpbin
  http:
  - route:
    - destination:
        host: httpbin
        subset: v1
      weight: 100
---
apiVersion: networking.istio.io/v1alpha3
kind: DestinationRule
metadata:
  name: httpbin
spec:
  host: httpbin
  subsets:
  - name: v1
    labels:
      version: v1
  - name: v2
    labels:
      version: v2
EOF

